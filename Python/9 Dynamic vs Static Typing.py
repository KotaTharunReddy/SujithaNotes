# About	
# If the type of the variable is checked at the runtime of the code than the language is called a dynamically typed language.	
# If the type of the variable is checked at the compile-time of the code than the language is called a statically typed language.

# Code Compilation/Interpretation	
# Code will get interpreted/compiled even if it contain error.	
# Code will not be compiled/interpreted until all the errors are corrected.

# Variable deceleration	
# Variable without its type is initialized.	
# Variable with its type is initialized.

